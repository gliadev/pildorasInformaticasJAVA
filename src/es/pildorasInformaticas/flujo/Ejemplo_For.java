public class Ejemplo_For {

	public static void main(String[] args) {
		
		for(int i = 0; i < 10; i++) {
			System.out.println("Este el bucle Nº: " + i);
			System.out.println("Hola Mundo");
			// con esto hacemos que cuando valga salga del bucle
			if(i==6) break;
		}

		
		//bucle for anidado
		for(int j = 0; j < 10; j++) {
			System.out.print("Hola");
		
	}
		{
			
		}
}
}

